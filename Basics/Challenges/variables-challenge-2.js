///BODMAS : 'Brackets, Order, Division, Multiplication, Addition, Subtraction'

let fahrenheit = 50;

// Calculate celsius & store in celsius variable 
// print the celcisius value 
// Formula F to C -> (Input°F − 32) × 5/9 = Output°C

let celsius = (fahrenheit - 32) * 5/9 

console.log(celsius, '°C')

// Calculate kelvin & store in kelvin variable 
// print the kelvin value 
// Formula F to K -> (Input°F − 32) × 5/9 + 273.15 = Output K

let kelvin = (fahrenheit - 32) * 5/9 + 273.15

console.log(kelvin, 'K')
