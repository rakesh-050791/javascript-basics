// Logical operators
let temp = 120;

// Logical AND operator : True if both sides are true, False otherwise
if (temp >= 60  && temp <= 90) {
    console.log(`It nice wether outside with temp ${temp}`)
}

// Logical OR operator : True if atleast one side is true, False otherwise

if (temp <= 0 || temp >= 100) {
    console.log("Don't go outside")
}